"""
    Sequences in Python
    Built In Multi Value Containers
    1. List
    2. Tuple
    3. Set
    4. Dictionary
    5. String

    Operations:
    1. Concatenation
    2. Repetition
    3. Membership Testing
    4. Slicing
    5. Indexing

"""

students = ["John", "Jennie", "Jim", "Jack", "Joe"]

# 1. Concatenation
print(students + ["Kia", "Mike", "Leo"])

# 2. Repetition
print(students * 2)

# 3. Membership Testing
print("John" in students)
print("Rahul" not in students)

# 4. Slicing
print(students[1 : 4])

# 5. Indexing
print(students[0])

print(len(students))

print(students[ len(students)-1 ])

















